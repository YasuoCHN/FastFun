package com.klw.fastfun.pay.common.domain;

import java.io.Serializable;

@SuppressWarnings("serial")
public class CodeOnlyInfo implements Serializable {
	
	private String spId;
	private String name;
	private String backUrl;
	private int synRadio;
	private int reqMethod;
	
	public String getSpId() {
		return spId;
	}
	public void setSpId(String spId) {
		this.spId = spId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBackUrl() {
		return backUrl;
	}
	public void setBackUrl(String backUrl) {
		this.backUrl = backUrl;
	}
	public int getSynRadio() {
		return synRadio;
	}
	public void setSynRadio(int synRadio) {
		this.synRadio = synRadio;
	}
	public int getReqMethod() {
		return reqMethod;
	}
	public void setReqMethod(int reqMethod) {
		this.reqMethod = reqMethod;
	}

}
