package com.klw.fastfun.pay.common.domain;

import java.io.Serializable;

public class PayMLHDReqInfo implements Serializable {
	/***
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	/** App在BeeCloud平台的唯一标识 */
	private String app_id;
	/** 签名生成时间时间戳，毫秒数 */
	private Long timestamp;
	/** 加密签名32位16进制格式,不区分大小写 */
	private String app_sign;
	/** 渠道类型 */
	private String channel;
	/** 订单总金额 */
	private int total_fee;
	/** 商户订单号 */
	private String bill_no;
	/** 订单标题 */
	private String title;
	/** 商户自定义回调地址 */
	private String notify_url;
	/** 订单失效时间 单位为秒 */
	private int bill_timeout;
	
	public String getApp_id() {
		return app_id;
	}
	public void setApp_id(String app_id) {
		this.app_id = app_id;
	}
	public Long getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(Long timestamp) {
		this.timestamp = timestamp;
	}
	public String getApp_sign() {
		return app_sign;
	}
	public void setApp_sign(String app_sign) {
		this.app_sign = app_sign;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public int getTotal_fee() {
		return total_fee;
	}
	public void setTotal_fee(int total_fee) {
		this.total_fee = total_fee;
	}
	public String getBill_no() {
		return bill_no;
	}
	public void setBill_no(String bill_no) {
		this.bill_no = bill_no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getNotify_url() {
		return notify_url;
	}
	public void setNotify_url(String notify_url) {
		this.notify_url = notify_url;
	}
	public int getBill_timeout() {
		return bill_timeout;
	}
	public void setBill_timeout(int bill_timeout) {
		this.bill_timeout = bill_timeout;
	}
	

}
